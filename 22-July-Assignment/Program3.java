class TernaryOperator {

	public static void main(String[] args) {
	
		int x = 25;

		System.out.print(x+" : ");
		System.out.println((x >= 18) ? "Eligible for voting" : "Not eligible for voting");

		x = 17;
		System.out.print(x+" : ");
		System.out.println((x >= 18) ? "Eligible for voting" : "Not eligible for voting");
	}
}
